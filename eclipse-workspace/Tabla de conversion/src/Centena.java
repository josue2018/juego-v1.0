
public class Centena {
	public String centena(int c)
	{
		switch(c)
		{
		case 1:{
			return("cien");}
		case 2:{
			return("doscientos");}
		case 3:{
			return("trescientos");}
		case 4:{
			return("cuatrocientos");}
		case 5:{
			return("quinientos");}
		case 6:{
			return("seiscientos");}
		case 7:{
			return("setecientos");}
		case 8:{
			return("ochocientos");}
		case 9:{
			return("novecientos");}
			
			default:{
				return("");}
			}
	}

}
