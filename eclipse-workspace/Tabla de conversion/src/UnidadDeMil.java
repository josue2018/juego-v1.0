
public class UnidadDeMil {
	public String Umil(int um)
	{
		switch(um)
		{
		case 1:{
			return("mil");}
		case 2:{
			return("dos mil");}
		case 3:{
			return("tres mil");}
		case 4:{
			return("cuatro mil");}
		case 5:{
			return("cinco mil");}
		case 6:{
			return("seis mil");}
		case 7:{
			return("siete mil");}
		case 8:{
			return("ocho mil");}
		case 9:{
			return("nueve mil");}
			
			default:{
				return("");}
			}
	}

}
