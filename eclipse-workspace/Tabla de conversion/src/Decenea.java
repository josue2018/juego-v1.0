
public class Decenea {
	public String decena(int d) 
	{
		switch(d)
		{
		case 1:{
			return("Diez");}
		case 2:{
			return("veinte");}
		case 3:{
			return("treinta");}
		case 4:{
			return("cuarenta");}
		case 5:{
			return("cincuenta");}
		case 6:{
			return("sesente");}
		case 7:{
			return("setenta");}
		case 8:{
			return("ochenta");}
		case 9:{
			return("noventa");}
		case 11:{
			return("once");}
		case 12:{
			return("Doce");}
		case 13:{
			return("trece");}
		case 14:{
			return("catorce");}
		case 15:{
			return("quince");}
		case 16:{
			return("dieciseis");}
		case 17:{
			return("diecisiete");}
		case 18:{
			return("dieciocho");}
		case 19:{
			return("diecinueve");}
			default:{
				return("");}
			
		}
	}

}
